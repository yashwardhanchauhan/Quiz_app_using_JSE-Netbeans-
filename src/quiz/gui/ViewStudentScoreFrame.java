public void loadUI()
{
    txtUserid.setText(UserProfile.getUsername());
    isAdding=true;
    jcExamid.removeAllItems();
    jcExamid.addItem("ExamId");
    for(String exid:examidlist)
        jcExamid.addItem(exid);
    isAdding=false;
}

