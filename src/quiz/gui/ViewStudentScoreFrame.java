    private void jcExamidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcExamidActionPerformed
        
         if(isAdding==true)
        {
            return;
        }
        String examid=jcExamid.getSelectedItem().toString();
        if(examid.equals("ExamId"))
        {
            JOptionPane.showMessageDialog(null,"Please Select ExamId!","NO Exami Id Selected!!!",JOptionPane.INFORMATION_MESSAGE);
            return ;
        }
        try
        {
            StudentScore score=PerformanceDao.getScore(UserProfile.getUsername(),examid);
            txtLanguage.setText(String.valueOf(score.getPer()));
        
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Cannot connect to DB","ERROR!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
        }
    }//GEN-LAST:event_jcExamidActionPerformed

